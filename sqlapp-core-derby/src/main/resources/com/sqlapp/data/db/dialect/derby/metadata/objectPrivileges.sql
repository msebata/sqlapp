SELECT 
   P.SELECTPRIV
   P.DELETEPRIV
   P.INSERTPRIV
   P.UPDATEPRIV
   P.REFERENCEPRIV
   P.TRIGGERPRIV
 , T.TABLENAME AS TABLE_NAME
 , S.SCHEMANAME AS SCHEMA_NAME
FROM SYS.SYSTABLEPERMS P
INNER JOIN SYS.SYSTABLES T
  ON (P.TABLEID=T.TABLEID)
INNER JOIN SYS.SYSSCHEMAS S
  ON (T.SCHEMAID=S.SCHEMAID)
WHERE 1=1
  /*if isNotEmpty(schemaName) */
  AND S.SCHEMANAME IN /*schemaName*/('%')
  /*end*/
  /*if isNotEmpty(objectName) */
  AND T.TABLENAME IN /*objectName*/('%')
  /*end*/
ORDER BY grantor, grantee, S.SCHEMANAME, T.TABLENAME
