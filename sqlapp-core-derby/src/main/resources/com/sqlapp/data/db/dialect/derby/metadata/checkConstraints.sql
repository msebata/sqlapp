SELECT C.CHECKDEFINITION
, CON.CONSTRAINTNAME
, CON.STATE
, T.TABLENAME
, COL.COLUMNNAME
, S.SCHEMANAME
FROM SYS.SYSCHECKS C
INNER JOIN SYS.SYSCONSTRAINTS CON
  ON (C.CONSTRAINTID=CON.CONSTRAINTID)
INNER JOIN SYS.SYSTABLES T
  ON (CON.TABLEID=T.TABLEID)
INNER JOIN SYS.SYSSCHEMAS S
  ON (CON.SCHEMAID=S.SCHEMAID)
INNER JOIN SYS.SYSCOLUMNS COL
  ON (T.TABLEID=COL.REFERENCEID)
WHERE T.TABLETYPE IN ('T', 'V')
  /*if isNotEmpty(schemaName)*/
  AND S.SCHEMANAME IN /*schemaName*/('%')
  /*end*/
  /*if isNotEmpty(tableName)*/
  AND T.TABLENAME IN /*tableName*/('%')
  /*end*/
ORDER BY CON.CONSTRAINTNAME, S.SCHEMANAME, T.TABLENAME, CON.CONSTRAINTNAME
