SELECT TR.*
, S.SCHEMANAME
, T.TABLENAME
FROM SYS.SYSTRIGGERS TR
INNER JOIN SYS.SYSSCHEMAS S
  ON (TR.SCHEMAID=S.SCHEMAID)
INNER JOIN SYS.SYSTABLES T
  ON (TR.TABLEID=T.TABLEID)
WHERE 1=1
  /*if isNotEmpty(schemaName) */
  AND S.SCHEMANAME IN /*schemaName*/('%')
  /*end*/
  /*if isNotEmpty(triggerName) */
  AND T.TRIGGERNAME IN /*triggerName*/('%')
  /*end*/
ORDER BY S.SCHEMANAME, TR.TRIGGERNAME
