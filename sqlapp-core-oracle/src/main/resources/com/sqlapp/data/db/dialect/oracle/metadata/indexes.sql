SELECT
	A.*
	, B.COLUMN_NAME AS COLUMN_NAME
	, C.COLUMN_EXPRESSION
	, COALESCE(B.COLUMN_POSITION, C.COLUMN_POSITION)
	AS COLUMN_POSITION
	, B.DESCEND
FROM ALL_INDEXES A
LEFT OUTER JOIN ALL_IND_COLUMNS B
ON (
	A.OWNER=B.INDEX_OWNER
	AND
	A.INDEX_NAME=B.INDEX_NAME
	AND
	A.TABLE_NAME=B.TABLE_NAME
)
LEFT OUTER JOIN ALL_IND_EXPRESSIONS C
ON (
	A.OWNER=C.INDEX_OWNER
	AND
	A.INDEX_NAME=C.INDEX_NAME
	AND
	A.TABLE_NAME=C.TABLE_NAME
)
WHERE A.INDEX_TYPE NOT IN ('LOB') 
  /*if isNotEmpty(schemaName)*/
  AND A.OWNER IN /*schemaName*/('%')
  /*end*/
  /*if isNotEmpty(tableName)*/
  AND A.TABLE_NAME IN /*tableName*/('%')
  /*end*/
  /*if isNotEmpty(indexName)*/
  AND A.INDEX_NAME IN /*indexName*/('%')
  /*end*/
ORDER BY A.OWNER, A.INDEX_NAME, COALESCE(B.COLUMN_POSITION, C.COLUMN_POSITION)