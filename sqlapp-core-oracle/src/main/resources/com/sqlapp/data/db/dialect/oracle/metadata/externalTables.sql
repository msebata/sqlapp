SELECT
  A.*
, B.LOCATION
, B.DIRECTORY_OWNER
, B.DIRECTORY_NAME
, C.COMMENTS
FROM ALL_EXTERNAL_TABLES A
LEFT OUTER JOIN ALL_EXTERNAL_LOCATIONS B
ON (A.OWNER=B.OWNER
    AND
    A.TABLE_NAME=B.TABLE_NAME)
LEFT OUTER JOIN ALL_TAB_COMMENTS C
ON (A.OWNER=C.OWNER
    AND
    A.TABLE_NAME=C.TABLE_NAME
    AND
    C.TABLE_TYPE='TABLE'
    AND
    C.COMMENTS IS NOT NULL)
WHERE 1=1 
  /*if isNotEmpty(schemaName)*/
  AND A.OWNER IN /*schemaName*/('%')
  /*end*/
  /*if isNotEmpty(tableName)*/
  AND A.TABLE_NAME IN /*tableName*/('%')
  /*end*/
ORDER BY A.OWNER, A.TABLE_NAME
